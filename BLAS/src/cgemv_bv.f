     +                    , incx, beta, betab, y, yb, incy, nbdirs)
      IMPLICIT NONE
      INCLUDE 'DIFFSIZES.inc'
C  Hint: ISIZE2OFa should be the size of dimension 2 of array a
C  Hint: ISIZE1OFx should be the size of dimension 1 of array x
C  Hint: nbdirsmax should be the maximum number of differentiation directions
C
C  -- Reference BLAS level2 routine --
C  -- Reference BLAS is a software package provided by Univ. of Tennessee,    --
C  -- Univ. of California Berkeley, Univ. of Colorado Denver and NAG Ltd..--
C
C     .. Scalar Arguments ..
      COMPLEX alpha, beta
      COMPLEX alphab(nbdirsmax), betab(nbdirsmax)
      INTEGER incx, incy, lda, m, n
      CHARACTER trans
C     ..
C     .. Array Arguments ..
      COMPLEX a(lda, *), x(*), y(*)
      COMPLEX ab(nbdirsmax, lda, *), xb(nbdirsmax, *), yb(nbdirsmax, *)
C     ..
C
C  =====================================================================
C
C     .. Parameters ..
      COMPLEX one
      PARAMETER (one=(1.0e+0,0.0e+0))
      COMPLEX zero
      PARAMETER (zero=(0.0e+0,0.0e+0))
C     ..
C     .. Local Scalars ..
      COMPLEX temp
      COMPLEX tempb(nbdirsmax)
      INTEGER i, info, ix, iy, j, jx, jy, kx, ky, lenx, leny
      LOGICAL noconj
      EXTERNAL LSAME
      INTEGER ISIZE1OFX, ISIZE2OFA
C     ..
C     .. External Functions ..
      INTEGER get_ISIZE1OFX, get_ISIZE2OFA
      EXTERNAL get_ISIZE1OFX, get_ISIZE2OFA
      LOGICAL LSAME
C     ..
C     .. External Subroutines ..
      EXTERNAL XERBLA, check_ISIZE1OFX_initialized, check_ISIZE2OFA_initialized
C     ..
C     .. Intrinsic Functions ..
      INTRINSIC CONJG, MAX
      INTEGER max1
      INTEGER nd
      INTEGER*4 branch
      INTEGER ii2
      INTEGER ii1
      INTEGER nbdirs
C     ..
C
C     Test the input parameters.
C
C     Check 0 < nbdirs <= nbdirsmax (required by DIFFSIZES.inc)
      CALL check_ISIZE1OFX_initialized()
      CALL check_ISIZE2OFA_initialized()
      ISIZE1OFX = get_ISIZE1OFX()
      ISIZE2OFA = get_ISIZE2OFA()
      IF (nbdirs.LE.0 .OR. nbdirs.GT.nbdirsmax) THEN
        WRITE(*,'(A,I0,A,I0,A)') 'Error: nbdirs=', nbdirs,
     +  ' must be in 1..nbdirsmax=', nbdirsmax, '. Stopping.'
        STOP 1
      END IF
C
      info = 0
      IF (.NOT.LSAME(trans, 'N') .AND. (.NOT.LSAME(trans, 'T')) .AND. (
     +    .NOT.LSAME(trans, 'C'))) THEN
        CALL PUSHCONTROL3B(0)
        info = 1
      ELSE IF (m .LT. 0) THEN
        CALL PUSHCONTROL3B(1)
        info = 2
      ELSE IF (n .LT. 0) THEN
        CALL PUSHCONTROL3B(2)
        info = 3
      ELSE
        IF (1 .LT. m) THEN
          max1 = m
        ELSE
          max1 = 1
        END IF
        IF (lda .LT. max1) THEN
          CALL PUSHCONTROL3B(3)
          info = 6
        ELSE IF (incx .EQ. 0) THEN
          CALL PUSHCONTROL3B(4)
          info = 8
        ELSE IF (incy .EQ. 0) THEN
          CALL PUSHCONTROL3B(5)
          info = 11
        ELSE
          CALL PUSHCONTROL3B(5)
        END IF
      END IF
      IF (info .EQ. 0) THEN
C
C     Quick return if possible.
C
        IF ((m .EQ. 0 .OR. n .EQ. 0) .OR. (alpha .EQ. zero .AND. beta 
     +      .EQ. one)) THEN
          DO nd=1,nbdirsmax
            alphab(nd) = (0.0,0.0)
          ENDDO
          DO ii1=1,ISIZE1OFx
            DO nd=1,nbdirsmax
              xb(nd, ii1) = (0.0,0.0)
            ENDDO
          ENDDO
          DO nd=1,nbdirsmax
            betab(nd) = (0.0,0.0)
          ENDDO
          DO ii1=1,ISIZE2OFa
            DO ii2=1,lda
              DO nd=1,nbdirsmax
                ab(nd, ii2, ii1) = (0.0,0.0)
              ENDDO
            ENDDO
          ENDDO
        ELSE
C
          noconj = LSAME(trans, 'T')
C
C     Set  LENX  and  LENY, the lengths of the vectors x and y, and set
C     up the start points in  X  and  Y.
C
          IF (LSAME(trans, 'N')) THEN
            lenx = n
            leny = m
          ELSE
            lenx = m
            leny = n
          END IF
          IF (incx .GT. 0) THEN
            kx = 1
          ELSE
            kx = 1 - (lenx-1)*incx
          END IF
          IF (incy .GT. 0) THEN
            ky = 1
          ELSE
            ky = 1 - (leny-1)*incy
          END IF
C
C     Start the operations. In this version the elements of A are
C     accessed sequentially with one pass through A.
C
C     First form  y := beta*y.
C
          IF (beta .NE. one) THEN
            IF (incy .EQ. 1) THEN
              IF (beta .EQ. zero) THEN
                CALL PUSHCONTROL3B(0)
              ELSE
                CALL PUSHCONTROL3B(1)
              END IF
            ELSE
              iy = ky
              IF (beta .EQ. zero) THEN
                DO i=1,leny
                  CALL PUSHINTEGER4(iy)
                  iy = iy + incy
                ENDDO
                CALL PUSHCONTROL3B(2)
              ELSE
                DO i=1,leny
                  CALL PUSHCOMPLEX8(y(iy))
                  y(iy) = beta*y(iy)
                  CALL PUSHINTEGER4(iy)
                  iy = iy + incy
                ENDDO
                CALL PUSHCONTROL3B(3)
              END IF
            END IF
          ELSE
            CALL PUSHCONTROL3B(4)
          END IF
          IF (alpha .EQ. zero) THEN
            DO nd=1,nbdirsmax
              alphab(nd) = (0.0,0.0)
            ENDDO
            DO ii1=1,ISIZE1OFx
              DO nd=1,nbdirsmax
                xb(nd, ii1) = (0.0,0.0)
              ENDDO
            ENDDO
            DO ii1=1,ISIZE2OFa
              DO ii2=1,lda
                DO nd=1,nbdirsmax
                  ab(nd, ii2, ii1) = (0.0,0.0)
                ENDDO
              ENDDO
            ENDDO
          ELSE IF (LSAME(trans, 'N')) THEN
C
C        Form  y := alpha*A*x + y.
C
            jx = kx
            IF (incy .EQ. 1) THEN
              DO j=1,n
                CALL PUSHINTEGER4(jx)
                jx = jx + incx
              ENDDO
              DO nd=1,nbdirsmax
                alphab(nd) = (0.0,0.0)
              ENDDO
              DO ii1=1,ISIZE1OFx
                DO nd=1,nbdirsmax
                  xb(nd, ii1) = (0.0,0.0)
                ENDDO
              ENDDO
              DO ii1=1,ISIZE2OFa
                DO ii2=1,lda
                  DO nd=1,nbdirsmax
                    ab(nd, ii2, ii1) = (0.0,0.0)
                  ENDDO
                ENDDO
              ENDDO
              DO j=n,1,-1
                CALL POPINTEGER4(jx)
                temp = alpha*x(jx)
                DO nd=1,nbdirsmax
                  tempb(nd) = (0.0,0.0)
                ENDDO
                DO i=m,1,-1
                  DO nd=1,nbdirs
                    tempb(nd) = tempb(nd) + CONJG(a(i, j))*yb(nd, i)
                    ab(nd, i, j) = ab(nd, i, j) + CONJG(temp)*yb(nd, i)
                  ENDDO
                ENDDO
                DO nd=1,nbdirs
                  alphab(nd) = alphab(nd) + CONJG(x(jx))*tempb(nd)
                  xb(nd, jx) = xb(nd, jx) + CONJG(alpha)*tempb(nd)
                ENDDO
              ENDDO
            ELSE
              DO j=1,n
                iy = ky
                DO i=1,m
                  CALL PUSHINTEGER4(iy)
                  iy = iy + incy
                ENDDO
                CALL PUSHINTEGER4(jx)
                jx = jx + incx
              ENDDO
              DO nd=1,nbdirsmax
                alphab(nd) = (0.0,0.0)
              ENDDO
              DO ii1=1,ISIZE1OFx
                DO nd=1,nbdirsmax
                  xb(nd, ii1) = (0.0,0.0)
                ENDDO
              ENDDO
              DO ii1=1,ISIZE2OFa
                DO ii2=1,lda
                  DO nd=1,nbdirsmax
                    ab(nd, ii2, ii1) = (0.0,0.0)
                  ENDDO
                ENDDO
              ENDDO
              DO j=n,1,-1
                CALL POPINTEGER4(jx)
                temp = alpha*x(jx)
                DO nd=1,nbdirsmax
                  tempb(nd) = (0.0,0.0)
                ENDDO
                DO i=m,1,-1
                  CALL POPINTEGER4(iy)
                  DO nd=1,nbdirs
                    tempb(nd) = tempb(nd) + CONJG(a(i, j))*yb(nd, iy)
                    ab(nd, i, j) = ab(nd, i, j) + CONJG(temp)*yb(nd, iy)
                  ENDDO
                ENDDO
                DO nd=1,nbdirs
                  alphab(nd) = alphab(nd) + CONJG(x(jx))*tempb(nd)
                  xb(nd, jx) = xb(nd, jx) + CONJG(alpha)*tempb(nd)
                ENDDO
              ENDDO
            END IF
          ELSE
C
C        Form  y := alpha*A**T*x + y  or  y := alpha*A**H*x + y.
C
            jy = ky
            IF (incx .EQ. 1) THEN
              DO j=1,n
                CALL PUSHCOMPLEX8(temp)
                temp = zero
                IF (noconj) THEN
                  DO i=1,m
                    temp = temp + a(i, j)*x(i)
                  ENDDO
                  CALL PUSHCONTROL1B(0)
                ELSE
                  DO i=1,m
                    temp = temp + CONJG(a(i, j))*x(i)
                  ENDDO
                  CALL PUSHCONTROL1B(1)
                END IF
                CALL PUSHINTEGER4(jy)
                jy = jy + incy
              ENDDO
              DO nd=1,nbdirsmax
                alphab(nd) = (0.0,0.0)
              ENDDO
              DO ii1=1,ISIZE1OFx
                DO nd=1,nbdirsmax
                  xb(nd, ii1) = (0.0,0.0)
                ENDDO
              ENDDO
              DO ii1=1,ISIZE2OFa
                DO ii2=1,lda
                  DO nd=1,nbdirsmax
                    ab(nd, ii2, ii1) = (0.0,0.0)
                  ENDDO
                ENDDO
              ENDDO
              DO j=n,1,-1
                CALL POPINTEGER4(jy)
                DO nd=1,nbdirs
                  alphab(nd) = alphab(nd) + CONJG(temp)*yb(nd, jy)
                  tempb(nd) = CONJG(alpha)*yb(nd, jy)
                ENDDO
                CALL POPCONTROL1B(branch)
                IF (branch .EQ. 0) THEN
                  DO i=m,1,-1
                    DO nd=1,nbdirs
                      ab(nd, i, j) = ab(nd, i, j) + CONJG(x(i))*tempb(nd
     +                  )
                      xb(nd, i) = xb(nd, i) + CONJG(a(i, j))*tempb(nd)
                    ENDDO
                  ENDDO
                ELSE
                  DO i=m,1,-1
                    DO nd=1,nbdirs
                      ab(nd, i, j) = ab(nd, i, j) + CONJG(CONJG(x(i))*
     +                  tempb(nd))
                      xb(nd, i) = xb(nd, i) + CONJG(CONJG(a(i, j)))*
     +                  tempb(nd)
                    ENDDO
                  ENDDO
                END IF
                CALL POPCOMPLEX8(temp)
              ENDDO
            ELSE
              DO j=1,n
                CALL PUSHCOMPLEX8(temp)
                temp = zero
                ix = kx
                IF (noconj) THEN
                  DO i=1,m
                    temp = temp + a(i, j)*x(ix)
                    CALL PUSHINTEGER4(ix)
                    ix = ix + incx
                  ENDDO
                  CALL PUSHCONTROL1B(0)
                ELSE
                  DO i=1,m
                    temp = temp + CONJG(a(i, j))*x(ix)
                    CALL PUSHINTEGER4(ix)
                    ix = ix + incx
                  ENDDO
                  CALL PUSHCONTROL1B(1)
                END IF
                CALL PUSHINTEGER4(jy)
                jy = jy + incy
              ENDDO
              DO nd=1,nbdirsmax
                alphab(nd) = (0.0,0.0)
              ENDDO
              DO ii1=1,ISIZE1OFx
                DO nd=1,nbdirsmax
                  xb(nd, ii1) = (0.0,0.0)
                ENDDO
              ENDDO
              DO ii1=1,ISIZE2OFa
                DO ii2=1,lda
                  DO nd=1,nbdirsmax
                    ab(nd, ii2, ii1) = (0.0,0.0)
                  ENDDO
                ENDDO
              ENDDO
              DO j=n,1,-1
                CALL POPINTEGER4(jy)
                DO nd=1,nbdirs
                  alphab(nd) = alphab(nd) + CONJG(temp)*yb(nd, jy)
                  tempb(nd) = CONJG(alpha)*yb(nd, jy)
                ENDDO
                CALL POPCONTROL1B(branch)
                IF (branch .EQ. 0) THEN
                  DO i=m,1,-1
                    CALL POPINTEGER4(ix)
                    DO nd=1,nbdirs
                      ab(nd, i, j) = ab(nd, i, j) + CONJG(x(ix))*tempb(
     +                  nd)
                      xb(nd, ix) = xb(nd, ix) + CONJG(a(i, j))*tempb(nd)
                    ENDDO
                  ENDDO
                ELSE
                  DO i=m,1,-1
                    CALL POPINTEGER4(ix)
                    DO nd=1,nbdirs
                      ab(nd, i, j) = ab(nd, i, j) + CONJG(CONJG(x(ix))*
     +                  tempb(nd))
                      xb(nd, ix) = xb(nd, ix) + CONJG(CONJG(a(i, j)))*
     +                  tempb(nd)
                    ENDDO
                  ENDDO
                END IF
                CALL POPCOMPLEX8(temp)
              ENDDO
            END IF
          END IF
          CALL POPCONTROL3B(branch)
          IF (branch .LT. 2) THEN
            IF (branch .EQ. 0) THEN
              DO i=leny,1,-1
                DO nd=1,nbdirs
                  yb(nd, i) = (0.0,0.0)
                ENDDO
              ENDDO
              DO nd=1,nbdirsmax
                betab(nd) = (0.0,0.0)
              ENDDO
            ELSE
              DO nd=1,nbdirsmax
                betab(nd) = (0.0,0.0)
              ENDDO
              DO i=leny,1,-1
                DO nd=1,nbdirs
                  betab(nd) = betab(nd) + CONJG(y(i))*yb(nd, i)
                  yb(nd, i) = CONJG(beta)*yb(nd, i)
                ENDDO
              ENDDO
            END IF
          ELSE IF (branch .EQ. 2) THEN
            DO i=leny,1,-1
              CALL POPINTEGER4(iy)
              DO nd=1,nbdirs
                yb(nd, iy) = (0.0,0.0)
              ENDDO
            ENDDO
            DO nd=1,nbdirsmax
              betab(nd) = (0.0,0.0)
            ENDDO
          ELSE IF (branch .EQ. 3) THEN
            DO nd=1,nbdirsmax
              betab(nd) = (0.0,0.0)
            ENDDO
            DO i=leny,1,-1
              CALL POPINTEGER4(iy)
              CALL POPCOMPLEX8(y(iy))
              DO nd=1,nbdirs
                betab(nd) = betab(nd) + CONJG(y(iy))*yb(nd, iy)
                yb(nd, iy) = CONJG(beta)*yb(nd, iy)
              ENDDO
            ENDDO
          ELSE
            DO nd=1,nbdirsmax
              betab(nd) = (0.0,0.0)
            ENDDO
          END IF
        END IF
      END IF
      CALL POPCONTROL3B(branch)
      END

