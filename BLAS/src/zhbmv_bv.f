                xb(nd, jx) = xb(nd, jx) + CONJG(alpha)*temp1b(nd)
              ENDDO
            ENDDO
          END IF
          CALL POPCONTROL3B(branch)
          IF (branch .LT. 2) THEN
            IF (branch .EQ. 0) THEN
              DO i=n,1,-1
                DO nd=1,nbdirs
                  yb(nd, i) = (0.0,0.0)
                ENDDO
              ENDDO
              DO nd=1,nbdirsmax
                betab(nd) = (0.0,0.0)
              ENDDO
            ELSE
              DO nd=1,nbdirsmax
                betab(nd) = (0.0,0.0)
              ENDDO
              DO i=n,1,-1
                DO nd=1,nbdirs
                  betab(nd) = betab(nd) + CONJG(y(i))*yb(nd, i)
                  yb(nd, i) = CONJG(beta)*yb(nd, i)
                ENDDO
              ENDDO
            END IF
          ELSE IF (branch .EQ. 2) THEN
            DO i=n,1,-1
              CALL POPINTEGER4(iy)
              DO nd=1,nbdirs
                yb(nd, iy) = (0.0,0.0)
              ENDDO
            ENDDO
            DO nd=1,nbdirsmax
              betab(nd) = (0.0,0.0)
            ENDDO
          ELSE IF (branch .EQ. 3) THEN
            DO nd=1,nbdirsmax
              betab(nd) = (0.0,0.0)
            ENDDO
            DO i=n,1,-1
              CALL POPINTEGER4(iy)
              CALL POPCOMPLEX16(y(iy))
              DO nd=1,nbdirs
                betab(nd) = betab(nd) + CONJG(y(iy))*yb(nd, iy)
                yb(nd, iy) = CONJG(beta)*yb(nd, iy)
              ENDDO
            ENDDO
          ELSE
            DO nd=1,nbdirsmax
              betab(nd) = (0.0,0.0)
            ENDDO
          END IF
        END IF
      END IF
      CALL POPCONTROL3B(branch)
      END

