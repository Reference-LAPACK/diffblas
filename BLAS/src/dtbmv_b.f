                temp = temp + a(l+i, j)*x(ix)
                CALL PUSHINTEGER4(ix)
                ix = ix + incx
              ENDDO
              CALL PUSHINTEGER4(i - 1)
              CALL PUSHINTEGER4(ad_from6)
              CALL PUSHREAL8(x(jx))
              x(jx) = temp
              CALL PUSHINTEGER4(jx)
              jx = jx + incx
            ENDDO
            DO ii1=1,ISIZE2OFa
              DO ii2=1,lda
                ab(ii2, ii1) = 0.D0
              ENDDO
            ENDDO
            DO j=n,1,-1
              CALL POPINTEGER4(jx)
              CALL POPREAL8(x(jx))
              tempb = xb(jx)
              xb(jx) = 0.D0
              l = 1 - j
              CALL POPINTEGER4(ad_from6)
              CALL POPINTEGER4(ad_to6)
              DO i=ad_to6,ad_from6,-1
                CALL POPINTEGER4(ix)
                ab(l+i, j) = ab(l+i, j) + x(ix)*tempb
                xb(ix) = xb(ix) + a(l+i, j)*tempb
              ENDDO
