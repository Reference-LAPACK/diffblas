              IF (branch .NE. 0) THEN
                CALL POPREAL8(temp)
                ab(1, j) = ab(1, j) + temp*tempb
                tempb = a(1, j)*tempb
              END IF
              xb(jx) = xb(jx) + tempb
            ENDDO
          END IF
          CALL POPCONTROL1B(branch)
        END IF
