                    tmp2 = b(i, j) - a(k, j)*b(i, k)
                    CALL PUSHCOMPLEX16(b(i, j))
                    b(i, j) = tmp2
                  ENDDO
                  CALL PUSHCONTROL1B(0)
                ELSE
                  CALL PUSHCONTROL1B(1)
                END IF
              ENDDO
              CALL PUSHINTEGER4(ad_from2)
              IF (nounit) THEN
                CALL PUSHCOMPLEX16(temp)
                temp = one/a(j, j)
                DO i=1,m
                  CALL PUSHCOMPLEX16(b(i, j))
                  b(i, j) = temp*b(i, j)
                ENDDO
                CALL PUSHCONTROL1B(0)
              ELSE
                CALL PUSHCONTROL1B(1)
              END IF
