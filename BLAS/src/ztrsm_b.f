          DO k=n,1,-1
            IF (nounit) THEN
              IF (noconj) THEN
                CALL PUSHCOMPLEX16(temp)
                temp = one/a(k, k)
                CALL PUSHCONTROL1B(1)
              ELSE
                CALL PUSHCOMPLEX16(temp)
                temp = one/DCONJG(a(k, k))
                CALL PUSHCONTROL1B(0)
              END IF
              DO i=1,m
                CALL PUSHCOMPLEX16(b(i, k))
                b(i, k) = temp*b(i, k)
              ENDDO
