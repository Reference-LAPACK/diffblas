                  temp = a(j, k)
                  CALL PUSHCONTROL1B(1)
                ELSE
                  CALL PUSHCOMPLEX16(temp)
                  temp = DCONJG(a(j, k))
                  CALL PUSHCONTROL1B(0)
                END IF
                DO i=1,m
                  tmp3 = b(i, j) - temp*b(i, k)
                  CALL PUSHCOMPLEX16(b(i, j))
                  b(i, j) = tmp3
                ENDDO
                CALL PUSHCONTROL1B(0)
              ELSE
                CALL PUSHCONTROL1B(1)
              END IF
