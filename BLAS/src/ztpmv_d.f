                  tempd = tempd + x(ix)*DCONJG(apd(k)) + temp0*xd(ix)
                  temp = temp + temp0*x(ix)
                ENDDO
              END IF
              xd(jx) = tempd
              x(jx) = temp
              jx = jx + incx
              kk = kk + (n-j+1)
            ENDDO
          END IF
        END IF
